package com.nationstar.reportengine.model;

public class SQLArguments {
	
	private String argumentType;
	private String argumentFormat;
	private String argumentName;
	private String argumentValue;
	private String argumentMapper;
	
	public String getArgumentValue() {
		return argumentValue;
	}
	public void setArgumentValue(String argumentValue) {
		this.argumentValue = argumentValue;
	}
	
	public String getArgumentMapper() {
		return argumentMapper;
	}
	public void setArgumentMapper(String argumentMapper) {
		this.argumentMapper = argumentMapper;
	}
	public String getArgumentType() {
		return argumentType;
	}
	public void setArgumentType(String argumentType) {
		this.argumentType = argumentType;
	}
	public String getArgumentFormat() {
		return argumentFormat;
	}
	public void setArgumentFormat(String argumentFormat) {
		this.argumentFormat = argumentFormat;
	}
	public String getArgumentName() {
		return argumentName;
	}
	public void setArgumentName(String argumentName) {
		this.argumentName = argumentName;
	}

	
	
	
	
}
