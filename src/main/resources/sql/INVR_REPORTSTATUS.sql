CREATE TABLE INVR_REPORTSTATUS 
   (REPORT_ID NUMBER NOT NULL,
    REQUESTED_REPORT VARCHAR2(255),
	REPORT_NAME VARCHAR2(255),
	TASK_ID VARCHAR2(255),
	REPORT_TYPE VARCHAR2(255),
	REPORTING_MONTH VARCHAR2(255),
	REPORT_START_DATE DATE,
	REPORT_END_DATE DATE,
	REPORT_PROCESSING_BEGINTIME TIMESTAMP WITH TIME ZONE,
	REPORT_PROCESSING_ENDTIME TIMESTAMP WITH TIME ZONE,
	REPORT_GENERATED_FILENAME VARCHAR2(255),
	REPORT_FILE_VERSION_NO NUMBER,
	REQUESTED_BY_USER_ID VARCHAR2(255),
	STATUS VARCHAR2(255),
	PRIMARY KEY (REPORT_ID)
   );
